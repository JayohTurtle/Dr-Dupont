            <h5>Le détartrage :</h5>
            <p>Il faut savoir que même en brossant les dents de manière régulière et en utilisant du fil dentaire, cela n’enlève pas totalement la plaque dentaire. En effet, sachez que certaines zones de la gencive et de la dent sont difficiles à atteindre. De ce fait, des résidus alimentaires ainsi que des bactéries s’y installent et se minéralisent au fil des jours. Par la suite, ils se transforment en tartre. Ce dernier ne peut pas être éliminé par le simple brossage des dents. Le dentiste effectue, alors, au détartrage. Cette technique consiste principalement à enlever le tartre visible et non visible. En effet, elle est en mesure d’enlever le tartre présent sous la gencive pour garantir une bonne santé bucco-dentaire.</p>
        </article>
        <article class="mt-2">
            <h5>Le plombage et les composites :</h5>
            <p>Le plombage (appelé également amalgame) se présente comme l’un des soins dentaires les plus courants. Il vise principalement à traiter les caries causées par les attaques de bactéries sur l’émail dentaire. Il faut dire que ce soin consiste essentiellement à éliminer la partie atteinte et à la substituer par un plombage. Ce dernier est, en réalité, un alliage de divers métaux comme l’argent, le zinc, le cuivre et du mercure. Notons que de nos jours, cette technique n’est plus utilisée, et ce, à cause de la présence de mercure. Elle est remplacée par le composite. Il s’agit d’un matériau résineux inerte et esthétique.</p>
        </article>
        <article class="mt-2">
            <h5>La dévitalisation dentaire :</h5>
            <p>La dévitalisation dentaire est une opération utilisée principalement pour traiter certains cas de caries sévères proches de la pulpe dentaire. Elle peut également être utilisée en cas de chute sur une dent,  mais également de nécrose pulpaire. Cette méthode consiste à enlever le tissu pulpaire au niveau de la racine de la dent et au niveau de la couronne. Pour mieux vous expliquer, la dent est dépourvue de toutes les terminaisons nerveuses et vasculaires qui sont responsables de la sensibilité et des douleurs.</p>
        </article>
        <article class="mt-2">
            <h5>Les couronnes</h5>
            <p>Quand un incident cause une fracture d’une ou de plusieurs dents et par conséquent leurs dévitalisations, la pose d’une couronne dentaire s’avère indispensable. En effet, cela permet de corriger le dommage structurel. Vous devez savoir qu’il s’agit d’une prothèse dentaire artificielle en alumine ou en céramique (ou porcelaine), avec ou sans alliage métallique qui recouvre totalement ou partiellement la dent pour la protéger. Il faut dire qu’il s’agit de la meilleure solution sur le long terme pour préserver les dents fragiles.</p>
        </article>
        <article class="mt-2">
            <h5>Les prothèses dentaires</h5>
            <p>La pose d’une prothèse dentaire se présente comme  un soin nécessaire en cas de perte. Généralement, on utilise cette solution en cas de perte de plusieurs dents. Les prothèses dentaires sont généralement conçues en résine ou avec armature en métal (stellites). De plus, elles existent sous plusieurs formes: </p>
                <ul>
                    <li>les prothèses partielles provisoires en résine</li>
                    <li>les prothèses partielles permanentes avec armature en métal (stellites)</li>
                    <li>prothèses complètes en résine</li>
                    <li>les prothèses partielles ou totales sur implants</li>
                    <li>etc...</li>
                </ul>
        </article>
        <article class="mt-2">
            <h5>L’extraction dentaire</h5>
            <p>Quand il n’est plus possible de conserver la dent, une extraction est nécessaire. Elle est également nécessaire en cas de :</p>
            <ul>
                    <li>fracture de la racine</li>
                    <li>infection (Kystes dentaires)</li>
                    <li>dent sévèrement déchaussée et très mobile</li>
                    <li>dent incluse</li>
                    <li>accident d’évolution des dents de sagesse</li>
                </ul>
